Map::Map() {
	dirt = TextureManager::LoadText("assets/destructibleWall.png");
	wall = TextureManager::LoadText("assets/wall.png");
	water = TextureManager::LoadText("assets/water.png");

	LoadMap("assets/lvl1.txt");

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;
	coll.w = 14;
	coll.h = 14;
	dest.x = dest.y = 0;
}
Map::Map(const char* nivel) {
	dirt = TextureManager::LoadText("assets/destructibleWall.png");
	wall = TextureManager::LoadText("assets/wall.png");
	water = TextureManager::LoadText("assets/water.png");

	LoadMap(nivel);

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;
	coll.w = 14;
	coll.h = 14;
	dest.x = dest.y = 0;
}
void Map::LoadMap(const char *nivel) {
	ifstream file;
	file.open(nivel);

	for (int i = 0; i < 20; i++)
	{
		for (int j = 0; j < 25; j++)
		{
			file >> map[i][j];
		}
	}
	file.close();
}
void Map::DrawMap() {
	int type = 0;

	
	
	for (int i = 0; i < 20; i++)
	{
		for (int j = 0; j < 25; j++)
		{
			type = map[i][j];
			dest.x = j * 32;
			dest.y = i * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(water, src, dest);
				break;
			case 1:
				TextureManager::Draw(wall, src, dest);
				break;
			case 2:
				TextureManager::Draw(dirt, src, dest);
				break;
			default:
				break;
			}
		}
	}
}
